    def _add_fenestration(self):
        """Add windows based on WWR."""
        if not self.stories or not self.window_defs:
            return

        self.add_comment("--- Fenestration ---")
        self.add("")

        wwr = self.window_defs[0].get('wwr', 0.25)

        # Re-derive walls from the zone data to add windows
        cum_height_ft = 0.0
        for si, story in enumerate(self.stories):
            ftf = story.get('floor_to_floor', 10)
            ftc = story.get('floor_to_ceiling', 9)
            h = ft_to_m(ftc)

            for sp in story.get('spaces', []):
                space_name = sp.get('name', f'Space_{si}')
                zone_name = f"{space_name}".replace(' ', '_')
                verts = sp.get('vertices', [])
                verts_m = [[ft_to_m(v[0]), ft_to_m(v[1])] for v in verts]
                n_verts = len(verts_m)

                for wi in range(n_verts):
                    v1 = verts_m[wi]
                    v2 = verts_m[(wi + 1) % n_verts]
                    wall_name = f"{zone_name}_Wall_{wi+1}"

                    # Calculate wall width
                    wall_w = math.sqrt((v2[0]-v1[0])**2 + (v2[1]-v1[1])**2)
                    if wall_w < 1.0 or h < 1.0:
                        continue

                    # Window inset (0.5m from edges, 1m from floor, 0.3m from ceiling)
                    margin_h = 0.5
                    sill_h = 1.0
                    head_margin = 0.3
                    win_w = wall_w * math.sqrt(wwr)  # approximate
                    win_h = h * math.sqrt(wwr)
                    # Ensure reasonable dimensions
                    win_w = min(win_w, wall_w - 2 * margin_h)
                    win_h = min(win_h, h - sill_h - head_margin)
                    if win_w < 0.3 or win_h < 0.3:
                        continue

                    # Center the window horizontally
                    offset_h = (wall_w - win_w) / 2
                    # Direction vector along wall
                    dx = (v2[0] - v1[0]) / wall_w
                    dy = (v2[1] - v1[1]) / wall_w

                    # Window corners (4 vertices, from upper-left going clockwise from outside)
                    wx1 = v1[0] + dx * offset_h
                    wy1 = v1[1] + dy * offset_h
                    wx2 = v1[0] + dx * (offset_h + win_w)
                    wy2 = v1[1] + dy * (offset_h + win_w)

                    self.add(f"FenestrationSurface:Detailed,")
                    self.add(f"  {wall_name}_Win,         !- Name")
                    self.add(f"  Window,                  !- Surface Type")
                    self.add(f"  WindowConstruction,      !- Construction Name")
                    self.add(f"  {wall_name},              !- Building Surface Name")
                    self.add(f"  ,                         !- Outside Boundary Condition Object")
                    self.add(f"  ,                         !- View Factor to Ground")
                    self.add(f"  ,                         !- Frame and Divider Name")
                    self.add(f"  1,                        !- Multiplier")
                    self.add(f"  4,                        !- Number of Vertices")
                    self.add(f"  {wx1:.3f}, {wy1:.3f}, {sill_h + win_h:.3f},")
                    self.add(f"  {wx1:.3f}, {wy1:.3f}, {sill_h:.3f},")
                    self.add(f"  {wx2:.3f}, {wy2:.3f}, {sill_h:.3f},")
                    self.add(f"  {wx2:.3f}, {wy2:.3f}, {sill_h + win_h:.3f};")
                    self.add("")

            cum_height_ft += ftf

