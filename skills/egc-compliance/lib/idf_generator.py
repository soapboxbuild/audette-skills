def ft_to_m(ft: float) -> float:
    """Convert feet to meters."""
    return ft * 0.3048


def btu_to_w(btu: float) -> float:
    """Convert BTU/h to Watts."""
    return btu * 0.293071


def r_to_rsi(r_ip: float) -> float:
    """Convert R-value (IP) to RSI (m²·K/W)."""
    return r_ip * 0.176110


def u_ip_to_si(u_ip: float) -> float:
    """Convert U-factor (IP: Btu/h·ft²·°F) to SI (W/m²·K)."""
    return u_ip * 5.678263


def f_to_c(f: float) -> float:
    """Convert Fahrenheit to Celsius."""
    return (f - 32) * 5 / 9


def cfm_to_m3s(cfm: float) -> float:
    """Convert CFM to m³/s."""
    return cfm * 0.000471947


def sqft_to_m2(sqft: float) -> float:
    """Convert square feet to square meters."""
    return sqft * 0.092903


class IDFGenerator:
    """Generate EnergyPlus IDF files from building model data."""

    def __init__(self, payload: Dict[str, Any]):
        """
        Initialize generator with building model payload.

        Args:
            payload: Building model dict with keys:
                - project: {name, climate_zone, building_type, units, north_axis}
                - stories: [{name, floor_to_floor, floor_to_ceiling, spaces: [...]}]
                - constructions: [{name, wall_type, wall_r_value, roof_r_value, ...}]
                - window_defs: [{name, wwr, u_factor, shgc, vlt}]
                - hvac_equipment: [{name, equipment_type, capacity_btuh, ...}]
                - thermal_zones: [{name, heating_setpoint, cooling_setpoint, ...}]
        """
        self.project = payload.get('project', {})
        self.stories = payload.get('stories', [])
        self.constructions = payload.get('constructions', [])
        self.window_defs = payload.get('window_defs', [])
        self.hvac_equipment = payload.get('hvac_equipment', [])
        self.thermal_zones = payload.get('thermal_zones', [])
        self.lines: List[str] = []
        self.zone_names: List[str] = []
        self.zone_floor_areas: Dict[str, float] = {}  # zone_name -> m²

    def add(self, text: str):
        """Add raw IDF text."""
        self.lines.append(text)

    def add_comment(self, text: str):
        """Add a comment line."""
        self.lines.append(f"! {text}")

    def generate(self) -> str:
        """Generate the complete IDF string."""
        self._add_header()
        self._add_simulation_control()
        self._add_building()
        self._add_timestep()
        self._add_run_period()
        self._add_ground_temperatures()
        self._add_schedules()
        self._add_materials_and_constructions()
        self._add_zones_and_geometry()
        self._add_fenestration()
        self._add_internal_loads()
        self._add_hvac()
        self._add_output_variables()
        return "\n".join(self.lines)

    def _add_header(self):
        """Add IDF header with project metadata."""
        self.add_comment("=" * 70)
        self.add_comment(f"  Audette EGC Platform — EnergyPlus Model")
        self.add_comment(f"  Project: {self.project.get('name', 'Untitled')}")
        self.add_comment(f"  Climate Zone: {self.project.get('climate_zone', 'N/A')}")
        self.add_comment(f"  Building Type: {self.project.get('building_type', 'N/A')}")
        self.add_comment(f"  Generated by Audette EGC Platform")
        self.add_comment("=" * 70)
        self.add("")

    def _add_simulation_control(self):
        """Add Version and SimulationControl objects."""
        self.add("Version,24.1;")
        self.add("")
        self.add("SimulationControl,")
        self.add("  No,                      !- Do Zone Sizing Calculation")
        self.add("  No,                      !- Do System Sizing Calculation")
        self.add("  No,                      !- Do Plant Sizing Calculation")
        self.add("  Yes,                     !- Run Simulation for Sizing Periods")
        self.add("  Yes;                     !- Run Simulation for Weather File Run Periods")
        self.add("")

    def _add_building(self):
        """Add Building object."""
        name = self.project.get('name', 'Building')
        north = self.project.get('north_axis', 0)
        self.add(f"Building,")
        self.add(f"  {name},                  !- Name")
        self.add(f"  {north},                 !- North Axis (deg)")
        self.add(f"  City,                    !- Terrain")
        self.add(f"  0.04,                    !- Loads Convergence Tolerance Value")
        self.add(f"  0.4,                     !- Temperature Convergence Tolerance Value (deltaC)")
        self.add(f"  FullExterior,            !- Solar Distribution")
        self.add(f"  25,                      !- Maximum Number of Warmup Days")
        self.add(f"  6;                       !- Minimum Number of Warmup Days")
        self.add("")

    def _add_timestep(self):
        """Add Timestep object."""
        self.add("Timestep,6;")
        self.add("")

