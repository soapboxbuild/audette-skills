        """Add interior wall and floor constructions."""
        self.add(f"Material,")
        self.add(f"  IntGypsum,               !- Name")
        self.add(f"  MediumSmooth,            !- Roughness")
        self.add(f"  0.0159,                  !- Thickness (m)")
        self.add(f"  0.16,                    !- Conductivity (W/m-K)")
        self.add(f"  784,                     !- Density (kg/m3)")
        self.add(f"  830;                     !- Specific Heat (J/kg-K)")
        self.add("")
        self.add("Construction,")
        self.add("  InteriorWall,            !- Name")
        self.add("  IntGypsum,               !- Outside Layer")
        self.add("  IntGypsum;               !- Layer 2")
        self.add("")
        self.add("Construction,")
        self.add("  InteriorFloor,           !- Name")
        self.add("  IntGypsum;               !- Layer 1")
