    def _add_ground_temperatures(self):
        """Add Site:GroundTemperature:BuildingSurface with climate-appropriate values."""
        # Monthly ground temps in Celsius, varies by climate zone
        cz = self.project.get('climate_zone', '5A')

        if cz.startswith('5'):
            # Zone 5 (Cool-Humid, Midwest)
            temps = "3.5, 2.5, 4.0, 7.5, 13.0, 17.5, 20.0, 20.5, 18.0, 13.5, 8.5, 5.0"
        elif cz.startswith('4'):
            # Zone 4 (Mixed-Humid, Mid-Atlantic)
            temps = "7.0, 5.5, 7.0, 10.5, 16.0, 20.5, 23.0, 23.5, 21.0, 16.5, 11.5, 8.0"
        elif cz.startswith('3'):
            # Zone 3 (Warm-Humid, Southeast)
            temps = "10.0, 9.0, 10.5, 13.5, 18.0, 22.5, 25.0, 25.5, 23.0, 18.5, 13.5, 10.5"
        elif cz.startswith('2'):
            # Zone 2 (Hot-Humid, Gulf Coast)
            temps = "15.0, 14.0, 15.0, 17.5, 21.5, 25.0, 27.0, 27.5, 25.5, 22.0, 18.0, 15.5"
        elif cz.startswith('1'):
            # Zone 1 (Very Hot-Humid, South Florida)
            temps = "20.0, 19.5, 20.0, 21.5, 24.0, 26.5, 28.0, 28.5, 27.0, 24.5, 22.0, 20.5"
        elif cz.startswith('6'):
            # Zone 6 (Cold-Humid, Northern states)
            temps = "0.5, -0.5, 1.0, 5.0, 10.5, 15.0, 17.5, 18.0, 15.5, 11.0, 6.0, 2.5"
        elif cz.startswith('7') or cz.startswith('8'):
            # Zone 7/8 (Very Cold, Subarctic)
            temps = "-3.0, -4.0, -2.0, 2.0, 7.5, 12.0, 14.5, 15.0, 12.5, 8.0, 3.0, -1.0"
        else:
            # Default to Zone 5
            temps = "3.5, 2.5, 4.0, 7.5, 13.0, 17.5, 20.0, 20.5, 18.0, 13.5, 8.5, 5.0"

        self.add("Site:GroundTemperature:BuildingSurface,")
        self.add(f"  {temps};")
        self.add("")

