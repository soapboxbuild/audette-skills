def validate_idf(idf_content: str) -> tuple[bool, List[str]]:
    """
    Validate IDF content for required sections and basic syntax.

    Args:
        idf_content: IDF file content as string

    Returns:
        Tuple of (is_valid, error_messages)
    """
    errors = []

    # Required objects for a valid IDF
    required_objects = [
        'Version',
        'Building',
        'SimulationControl',
        'RunPeriod',
        'Timestep'
    ]

    # Check for required objects
    for obj in required_objects:
        if f'{obj},' not in idf_content:
            errors.append(f"Missing required object: {obj}")

    # Check for basic syntax errors
    lines = idf_content.split('\n')
    in_object = False
    for i, line in enumerate(lines):
        stripped = line.strip()
        if not stripped or stripped.startswith('!'):
            continue

        # Check for object start (ends with comma)
        if ',' in stripped and not in_object:
            # Could be object name or field
            if stripped.endswith(','):
                in_object = True

        # Check for object end (ends with semicolon)
        if ';' in stripped:
            in_object = False

    # If still in object at end, that's an error
    if in_object:
        errors.append("IDF has unclosed object (missing semicolon)")

    is_valid = len(errors) == 0
    return (is_valid, errors)


def write_idf(idf_content: str, output_dir: str, building_name: str, scenario: str = 'baseline') -> str:
    """
    Write IDF content to file with proper naming convention.

    Args:
        idf_content: IDF file content as string
        output_dir: Directory to write IDF file
        building_name: Building name for filename
        scenario: Scenario name (baseline, 90.1-2022, retrofit, etc.)

    Returns:
        Path to written IDF file
    """
    # Sanitize building name for filename
    safe_building_name = building_name.replace(' ', '_').replace('/', '_').replace('\\', '_')
    safe_scenario = scenario.replace(' ', '_').replace('/', '_').replace('\\', '_')

    # Construct filename
    filename = f"{safe_building_name}_{safe_scenario}.idf"
    file_path = os.path.join(output_dir, filename)

    # Write IDF content
    with open(file_path, 'w') as f:
        f.write(idf_content)

    return file_path
