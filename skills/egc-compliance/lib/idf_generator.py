    def _add_materials_and_constructions(self):
        """Add materials and constructions for envelope components."""
        self.add_comment("--- Materials & Constructions ---")
        self.add("")

        # For each construction in the model, create materials + construction
        for i, const in enumerate(self.constructions):
            wall_r = const.get('wall_r_value', 13)
            wall_ci = const.get('wall_ci_r_value', 0)
            roof_r = const.get('roof_r_value', 20)
            slab_r = const.get('slab_r_value', 0)
            wall_type = const.get('wall_type', 'mass')
            name = const.get('name', f'Construction_{i}')
            safe_name = name.replace(' ', '_').replace('-', '_')

            # Wall construction
            self._add_wall_construction(safe_name, wall_type, wall_r, wall_ci)
            # Roof construction
            self._add_roof_construction(safe_name, roof_r)
            # Floor construction
            self._add_floor_construction(safe_name, slab_r)

        # If no constructions defined, add defaults
        if not self.constructions:
            self._add_wall_construction("Default", "mass", 4.0, 0)
            self._add_roof_construction("Default", 10.0)
            self._add_floor_construction("Default", 0)

        # Interior constructions
        self._add_interior_constructions()

    def _add_wall_construction(self, prefix: str, wall_type: str, r_cavity: float, r_ci: float):
        """Create wall material layers and construction."""
        # Outer layer
        if wall_type == 'mass':
            self.add(f"Material,")
            self.add(f"  {prefix}_WallOuter,      !- Name")
            self.add(f"  MediumRough,             !- Roughness")
            self.add(f"  0.1016,                  !- Thickness (m) [4 in brick]")
            self.add(f"  0.89,                    !- Conductivity (W/m-K)")
            self.add(f"  1920,                    !- Density (kg/m3)")
            self.add(f"  790;                     !- Specific Heat (J/kg-K)")
            self.add("")
        else:  # steel_frame, wood_frame, metal_building
            self.add(f"Material,")
            self.add(f"  {prefix}_WallOuter,      !- Name")
            self.add(f"  MediumSmooth,            !- Roughness")
            self.add(f"  0.0127,                  !- Thickness (m) [0.5 in]")
            self.add(f"  0.16,                    !- Conductivity (W/m-K)")
            self.add(f"  784,                     !- Density (kg/m3)")
            self.add(f"  830;                     !- Specific Heat (J/kg-K)")
            self.add("")

        # Insulation layer — use R-value to compute thickness
        total_r_si = r_to_rsi(r_cavity + r_ci)
        if total_r_si < 0.1:
            total_r_si = 0.1  # minimum
        ins_conductivity = 0.04  # typical fiberglass/foam
        ins_thickness = total_r_si * ins_conductivity
        ins_thickness = max(0.01, min(0.5, ins_thickness))  # clamp

        self.add(f"Material,")
        self.add(f"  {prefix}_WallInsulation, !- Name")
        self.add(f"  MediumRough,             !- Roughness")
        self.add(f"  {ins_thickness:.4f},       !- Thickness (m)")
        self.add(f"  {ins_conductivity},        !- Conductivity (W/m-K)")
        self.add(f"  32,                       !- Density (kg/m3)")
        self.add(f"  830;                      !- Specific Heat (J/kg-K)")
        self.add("")

        # Inner layer (gypsum)
        self.add(f"Material,")
        self.add(f"  {prefix}_WallInner,      !- Name")
        self.add(f"  MediumSmooth,            !- Roughness")
        self.add(f"  0.0159,                  !- Thickness (m) [5/8 in gypsum]")
        self.add(f"  0.16,                    !- Conductivity (W/m-K)")
        self.add(f"  784,                     !- Density (kg/m3)")
        self.add(f"  830;                     !- Specific Heat (J/kg-K)")
        self.add("")

        # Wall construction
        self.add(f"Construction,")
        self.add(f"  {prefix}_Wall,           !- Name")
        self.add(f"  {prefix}_WallOuter,      !- Outside Layer")
        self.add(f"  {prefix}_WallInsulation, !- Layer 2")
        self.add(f"  {prefix}_WallInner;      !- Layer 3")
        self.add("")

    def _add_roof_construction(self, prefix: str, roof_r: float):
        """Create roof material layers and construction."""
        # Roof membrane
        self.add(f"Material,")
        self.add(f"  {prefix}_RoofMembrane,   !- Name")
        self.add(f"  VeryRough,               !- Roughness")
        self.add(f"  0.0095,                  !- Thickness (m)")
        self.add(f"  0.16,                    !- Conductivity (W/m-K)")
        self.add(f"  1121,                    !- Density (kg/m3)")
        self.add(f"  1460;                    !- Specific Heat (J/kg-K)")
        self.add("")

        # Roof insulation
        r_si = r_to_rsi(roof_r)
        if r_si < 0.1:
            r_si = 0.1
        ins_k = 0.04
        ins_t = r_si * ins_k
        ins_t = max(0.01, min(1.0, ins_t))

        self.add(f"Material,")
        self.add(f"  {prefix}_RoofInsulation, !- Name")
        self.add(f"  MediumRough,             !- Roughness")
        self.add(f"  {ins_t:.4f},              !- Thickness (m)")
        self.add(f"  {ins_k},                  !- Conductivity (W/m-K)")
        self.add(f"  32,                       !- Density (kg/m3)")
        self.add(f"  830;                      !- Specific Heat (J/kg-K)")
        self.add("")

        # Roof deck (concrete)
        self.add(f"Material,")
        self.add(f"  {prefix}_RoofDeck,       !- Name")
        self.add(f"  MediumRough,             !- Roughness")
        self.add(f"  0.1524,                  !- Thickness (m) [6 in concrete]")
        self.add(f"  1.311,                   !- Conductivity (W/m-K)")
        self.add(f"  2240,                    !- Density (kg/m3)")
        self.add(f"  900;                     !- Specific Heat (J/kg-K)")
        self.add("")

        self.add(f"Construction,")
        self.add(f"  {prefix}_Roof,           !- Name")
        self.add(f"  {prefix}_RoofMembrane,   !- Outside Layer")
        self.add(f"  {prefix}_RoofInsulation, !- Layer 2")
        self.add(f"  {prefix}_RoofDeck;       !- Layer 3")
        self.add("")

    def _add_floor_construction(self, prefix: str, slab_r: float):
        """Create floor construction."""
        # Concrete slab
        self.add(f"Material,")
        self.add(f"  {prefix}_Slab,           !- Name")
        self.add(f"  MediumRough,             !- Roughness")
        self.add(f"  0.2032,                  !- Thickness (m) [8 in concrete]")
        self.add(f"  1.311,                   !- Conductivity (W/m-K)")
        self.add(f"  2240,                    !- Density (kg/m3)")
        self.add(f"  900;                     !- Specific Heat (J/kg-K)")
        self.add("")

        if slab_r > 0:
            r_si = r_to_rsi(slab_r)
            ins_t = r_si * 0.04
            ins_t = max(0.01, min(0.5, ins_t))
            self.add(f"Material,")
            self.add(f"  {prefix}_SlabInsulation, !- Name")
            self.add(f"  MediumRough,             !- Roughness")
            self.add(f"  {ins_t:.4f},              !- Thickness (m)")
            self.add(f"  0.04,                    !- Conductivity (W/m-K)")
            self.add(f"  32,                       !- Density (kg/m3)")
            self.add(f"  830;                      !- Specific Heat (J/kg-K)")
            self.add("")
            self.add(f"Construction,")
            self.add(f"  {prefix}_Floor,          !- Name")
            self.add(f"  {prefix}_Slab,           !- Outside Layer")
            self.add(f"  {prefix}_SlabInsulation; !- Layer 2")
        else:
            self.add(f"Construction,")
            self.add(f"  {prefix}_Floor,          !- Name")
            self.add(f"  {prefix}_Slab;           !- Outside Layer")
        self.add("")

